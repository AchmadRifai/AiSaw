package achmad.rifai.aisaw.beans;

public class Syarat {
	private String cb,nama;
	private float kep;

	public Syarat() {
		super();
	}

	public String getCb() {
		return cb;
	}

	public void setCb(String cb) {
		this.cb = cb;
	}

	public String getNama() {
		return nama;
	}

	public void setNama(String nama) {
		this.nama = nama;
	}

	public float getKep() {
		return kep;
	}

	public void setKep(float kep) {
		this.kep = kep;
	}
}